"""
Вася решил передать Пете шифрограмму.
Поскольку о промышленных шифрах Вася ничего не знает,
он решил зашифровать сообщение следующим образом: он посылает Пете строку.

Каждый символ строки — либо буква, либо пробел, либо точка «.», либо две точки «..».
Если после какой-то буквы стоит точка, значит, мы оставляем букву без изменений
(об одной точке Вася задумался, чтобы усложнить расшифровку). Саму точку при этом надо удалить.
Если после какой-то буквы стоят две точки, то предыдущий символ надо стереть. Обе точки при этом тоже нужно удалить.
Возможна ситуация, когда сообщение после расшифровки будет пустым.
В качестве результата можно вернуть просто пустую строку.

Примеры шифровок-расшифровок:

абра-кадабра. → абра-кадабра
абраа..-кадабра → абра-кадабра
абраа..-.кадабра → абра-кадабра
абра--..кадабра → абра-кадабра
абрау...-кадабра → абра-кадабра (сначала срабатывает правило двух точек, потом правило одной точки)
абра........ → <пустая строка>
абр......a. → a
1..2.3 → 23
. → <пустая строка>
1....................... → <пустая строка>

Помогите Пете написать программу для расшифровки.
Напишите функцию decrypt, которая принимает на вход шифр в виде строки, а возвращает расшифрованное сообщение.

Программа должна работать через конвейер (pipe):

$ echo  ‘абраа..-.кадабра’ | python3 decrypt.py
абра-кадабра

Команда echo выводит текст (в стандартный поток вывода).
"""

import sys


def decrypt(encrypted_string):
    """
    Расшифровывает строку по правилам:
    - Одна точка после буквы: буква остается, точка удаляется
    - Две точки после буквы: удаляется предыдущий символ, обе точки удаляются

    Алгоритм работает за O(n) времени и использует O(n) памяти.
    """
    result = []
    i = 0

    while i < len(encrypted_string):
        char = encrypted_string[i]

        # Если текущий символ - точка, пропускаем (обрабатывается при проверке предыдущего)
        if char == '.':
            i += 1
            continue

        # Проверяем, есть ли после символа точки
        dots_count = 0
        pos = i + 1
        while pos < len(encrypted_string) and encrypted_string[pos] == '.':
            dots_count += 1
            pos += 1

        if dots_count == 2:
            # Две точки: удаляем последний символ
            if result:
                result.pop()
            i = pos  # Перемещаем указатель после двух точек
        elif dots_count == 1:
            # Одна точка: добавляем символ
            result.append(char)
            i = pos  # Перемещаем указатель после точки
        else:
            # Нет точек: добавляем символ
            result.append(char)
            i += 1

    return ''.join(result)


def run_tests():
    """Тестирует функцию на примерах из условия"""
    test_cases = [
        ("абра-кадабра.", "абра-кадабра"),
        ("абраа..-кадабра", "абра-кадабра"),
        ("абраа..-.кадабра", "абра-кадабра"),
        ("абра--..кадабра", "абра-кадабра"),
        ("абрау...-кадабра", "абра-кадабра"),
        ("абра........", ""),
        ("абр......a.", "a"),
        ("1..2.3", "23"),
        (".", ""),
        ("1.......................", ""),
        ("", ""),
        ("hello..world", "heworld"),
        ("a..b.c", "bc"),
    ]

    print("Запуск тестов...")
    all_passed = True

    for i, (encrypted, expected) in enumerate(test_cases, 1):
        result = decrypt(encrypted)
        if result == expected:
            print(f"Тест {i}: ✓ PASSED")
        else:
            print(f"Тест {i}: ✗ FAILED")
            print(f"  Вход: '{encrypted}'")
            print(f"  Ожидалось: '{expected}'")
            print(f"  Получено: '{result}'")
            all_passed = False

    if all_passed:
        print("\nВсе тесты пройдены успешно!")
    else:
        print("\nНекоторые тесты не пройдены.")

    return all_passed


if __name__ == '__main__':
    # Проверяем, был ли передан аргумент для тестирования
    if len(sys.argv) > 1 and sys.argv[1] == '--test':
        run_tests()
    else:
        # Читаем строку из стандартного ввода
        encrypted_text = sys.stdin.read().strip()

        # Расшифровываем
        decrypted_text = decrypt(encrypted_text)

        # Выводим результат
        print(decrypted_text)
