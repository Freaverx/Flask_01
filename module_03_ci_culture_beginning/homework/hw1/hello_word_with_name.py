"""
Реализуйте endpoint /hello-world/<имя>, который возвращает строку «Привет, <имя>. Хорошей пятницы!».
Вместо хорошей пятницы endpoint должен уметь желать хорошего дня недели в целом, на русском языке.

Пример запроса, сделанного в субботу:

/hello-world/Саша  →  Привет, Саша. Хорошей субботы!
"""

from datetime import datetime
from flask import Flask

app = Flask(__name__)

# Оптимальный способ хранения названий дней недели
# Используем кортеж, так как данные неизменяемы и занимают меньше памяти
# по сравнению со списком и словарём
WEEKDAYS_RU = (
    'понедельник',  # 0
    'вторник',  # 1
    'среда',  # 2
    'четверг',  # 3
    'пятница',  # 4
    'суббота',  # 5
    'воскресенье'  # 6
)


# Альтернативный вариант со словарём (если нужен более явный доступ)
# WEEKDAYS_RU = {
#     0: 'понедельник',
#     1: 'вторник',
#     2: 'среда',
#     3: 'четверг',
#     4: 'пятница',
#     5: 'суббота',
#     6: 'воскресенье'
# }

@app.route('/hello-world/<name>')
def hello_world(name):
    """
    Возвращает приветствие с именем и пожеланием хорошего дня недели

    Args:
        name (str): Имя пользователя

    Returns:
        str: Приветствие с пожеланием хорошего дня недели
    """
    # Получаем текущий день недели (0 - понедельник, 6 - воскресенье)
    weekday_index = datetime.today().weekday()

    # Получаем название дня недели из кортежа
    weekday_name = WEEKDAYS_RU[weekday_index]

    # Формируем ответ
    return f'Привет, {name}. Хорошего {weekday_name}!'


if __name__ == '__main__':
    app.run(debug=True)